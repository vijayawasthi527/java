package logical;

public class Swap_Numbers {

	public static void main(String[] args)
	{
		int a=100;
		int b=90;
		
		System.out.println("Before swapping values of a="+a+" and b="+b);


		
		int c=a;//100
		a=b;//90
		b=c;//100
		
		System.out.println("After swapping values of a="+a+" and b="+b);
		


	}

}
