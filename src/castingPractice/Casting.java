package castingPractice;

public class Casting {

	public static void main(String[] args) 
	{
		//implicit Casting
		
		int a=999;
		
		System.out.println(a);
		
		double b=a;
		System.out.println(b);
		
		System.out.println("============================");
		
		
		//explicit Casting
		
		double d=1999.9d;
		System.out.println(d);
		
		int e=(int) d;
		System.out.println(e);
		

	}

}
